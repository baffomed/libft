#include "libft.h"

char			**ft_strsplit(char const *s, char c)
{
	char	**words;
	char	*line;

	if (!s || !(line = ft_strdup((char *)s)))
		return (NULL);
	words = ft_get_warr(line, c, ft_word_count(line, c));
	free(line);
	return (words);
}
