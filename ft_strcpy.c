#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	size_t len;
	size_t i;

	len = ft_strlen(s2);
	i = 0;
	while (i < len)
	{
		*(s1 + i) = *(s2 + i);
		++i;
	}
	*(s1 + i) = '\0';
	return (s1);
}
