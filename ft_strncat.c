#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t j;

	j = ft_strlen(s1);
	i = 0;
	while (i < n)
	{
		if (!(*(s2 + i)))
			break ;
		*(s1 + j + i) = *(s2 + i);
		++i;
	}
	*(s1 + j + i) = '\0';
	return (s1);
}
